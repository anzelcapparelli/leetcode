var sumCount = {}

for (i = lowlimit; i < highlimit; i++) {
    var intArr = i.split;
    var intSum = 0;
    for (j = 0; j < intArr.length; j++) {
        intSum += j;
    }
    if (!sumCount.hasOwnProperty(intSum)) {
        sumCount.intSum;
    } else {
        sumCount.intSum++;
    }
}